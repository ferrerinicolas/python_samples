"""Write a program that has a Boolean variable that represents whether 
or not it is raining outside.


Your program should 

print I'm going to dance in the rain! if it is raining, and 
I'm going to dance in the sun! if it is not raining."""


its_raining = False

if its_raining:
    print ("I'm going to dance in the rain! if it is raining")
    
else:
    print ("I'm going to dance in the sun! if it is not raining.")
